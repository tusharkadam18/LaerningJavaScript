// we are going to see the types of variable declartion in javascript
// There are 3 Types of Variable declation in js
// var
var a = 10;
for (var a = 10; a < 20; a++) {
  console.log('a :>> ', a);
}
// outside the block also the value of a is incremented becouse of the var declartion.
console.log(a);
// let
let c = 10;
for (let a = 10; a < 20; a++) {
  console.log('a :>> ', c);
}
// const
const d = 10;
for (let b = 10; b < 20; b++) {
  console.log('d :>> ', d);
}

